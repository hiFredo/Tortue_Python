import turtle

# Créer une fenêtre et une tortue
t = turtle.Turtle()



for i in range(5) :   
    t.forward(50) #avance 
    t.left(90) #tourne gauche en degre 
    t.forward(50) #avance 
    t.right(90)     
t.forward(50)     
    

# Garder la fenêtre ouverte
turtle.done()


